/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  DisplayStatus = HAL_OK;
  EEpromStatus 	= HAL_OK;
  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_I2C1_Init();
  MX_RTC_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 2 */

  // Initialize settings
  EEpromStatus |= EEprom_Init();

  // Start HV Display
  DisplayStatus |= Display_Init( &htim1, &htim2 );
  Display_Set_Brightness( 	EEprom_Get_Brightness()  );
  Display_Set_RefreshRate( 	EEprom_Get_RefreshRate() );
  Display_Set_TimeFormat( 	EEprom_Get_TimeFormat()  );
  Display_Set_DotStyle(		EEprom_Get_DotStyle()	 );
  Display_Set_DotCustom( 	EEprom_Get_DotCustom() 	 );
  Display_Start();

  USB_Init();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  uint8_t OldSec = Display_Get_SL();
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
	  // React to incoming requests
	  if( USB_RX_IsReceived() ) USB_RX_Respond();

	  // Wait for a second
	  if( OldSec == Display_Get_SL() ) continue;
	  OldSec = Display_Get_SL();

	  //USB_TX_Info();
  }
  /* USER CODE END 3 */

}

